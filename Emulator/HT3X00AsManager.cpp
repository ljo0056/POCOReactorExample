void HT3X00AsManager::AddWork(int id, const std::function<int(void)>& work)
{
    WORK_INFO work_info;
    work_info.id = id;
    work_info.work = work;
    AddWork(work_info);
}

void HT3X00AsManager::AddWork(WORK_INFO& work_info)
{
    if (false == m_thread_run)
        return;

    std::lock_guard<std::recursive_mutex> lock(m_mutex);    
    m_works.push_back(work_info);
    m_work_event.WakeUp();
}

void HT3X00AsManager::DeleteAllWork()
{
    std::lock_guard<std::recursive_mutex> lock(m_mutex);
    m_works.clear();
}

void HT3X00AsManager::ProcWork()
{
    LOGV << fmt::format("Starting Thread... ");

    do
    {
        m_work_event.Wait();        
                
        while (m_thread_run == true)
        {
            WORK_INFO work_info;

            // Lock
            {
                std::lock_guard<std::recursive_mutex> lock(m_mutex);
                if (m_works.empty())
                    break;

                work_info = m_works.front();
                m_works.pop_front();
            }

            if (work_info.work != nullptr)
                work_info.work();
        }        

    } while (true == m_thread_run);

    LOGV << fmt::format("End Thread... ");
}

