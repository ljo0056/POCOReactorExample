int HT3X00AsManager::Parse(PocoSocket* socket, byte* buffer, int length, int& found_len)
{
    if (length < 18)
        return 0;

    int remain_len = length - found_len;
    int packet_min_len = XHt3000AsPacketExtention::PACKET_MIN_LEN;    // STX żÍ ETX Ć÷ÇÔÇŃ Packet ĂÖĽŇ ±ćŔĚ
    do
    {
        if (buffer[found_len] != XHt3000AsPacketExtention::STX)
        {
            found_len += remain_len; // Ăł¸® ľČµČ ąöĆŰ µĄŔĚĹÍ ąö¸˛
            return 1;
        }

        ReadOnlySpan slot(&buffer[found_len], remain_len);
        USHORT packet_length = XHt3000AsPacketExtention::PacketLength(slot);
        if (remain_len < packet_length + 2) // STX + ETX
            return 0;

        if (buffer[found_len + packet_length + 1] != XHt3000AsPacketExtention::ETX)
        {
            found_len += remain_len; // Ăł¸® ľČµČ ąöĆŰ µĄŔĚĹÍ ąö¸˛
            return 2;
        }

        slot.Set(&buffer[found_len], packet_length + 2); // STX + ETX Ć÷ÇÔ Slice

        auto checksum = slot.GetByte(slot.GetLength() - 3);
        auto checksum_calc = XHt3000AsPacketExtention::CalcChecksum(&buffer[found_len + 1], packet_length - 2);
        if (checksum != checksum_calc)
        {
            found_len += (packet_length + 2); // ÇŘ´ç ĆĐĹ¶ µĄŔĚĹÍ ąö¸˛
            continue;
        }

        XHt3000PacketCommand cmd = (XHt3000PacketCommand)XHt3000AsPacketExtention::OperatorCode(slot);
        if (m_command_name.find(cmd) != m_command_name.end())
        {
            if (!(cmd == XHt3000CmdReadSampler || cmd == XHt3000CmdReadSamplerEx2))
                LOGI << fmt::format("Received Command [{}]", m_command_name[cmd]);
        }

        switch (cmd)
        {
        case XHt3000CmdReadInformation:
        case XHt3000CmdReadSerial:
        case XHt3000CmdSetEthernet:
        case XHt3000CmdReadEthernet:
            OnPacketHandler(cmd, socket, m_packet_inform.get(), slot, cmd == XHt3000CmdReadInformation ? true : false);
            break;

        case XHt3000CmdReadBCR:
        case XHt3000CmdReadRFID:
        case XHt3000CmdReadSetupEx:
        case XHt3000CmdSetSetupEx:
        case XHt3000CmdReadSetupEx2:
        case XHt3000CmdSetSetupEx2:
            OnPacketHandler(cmd, socket, m_packet_config.get(), slot, true);
            break;

        case XHt3000CmdReadMethod:
        case XHt3000CmdWriteMethod:
        case XHt3000CmdReadMethodEx:
        case XHt3000CmdWriteMethodEx:
            OnPacketHandler(cmd, socket, m_packet_setup.get(), slot, true);
            break;

        case XHt3000CmdReadSampler:
        case XHt3000CmdReadSamplerEx:
        case XHt3000CmdReadSamplerEx2:
            OnPacketHandler(cmd, socket, m_packet_state.get(), slot, false);
            break;

        case XHt3000CmdSingleRunEx2:
            OnSingleRunEx2(socket, slot);
            break;
        case XHt3000CmdAbort:
            OnAbort(socket, slot);
            break;
        default:
            break;
        }

        found_len += (packet_length + 2);
        remain_len = length - found_len;

    } while (packet_min_len <= remain_len);

    return 0;
}

