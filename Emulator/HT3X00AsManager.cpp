void HT3X00AsManager::onReadable(PocoSocket* socket, byte* buffer, int length)
{
    if (length <= 0)
        return;

    //LOGI << fmt::format("Read data length[{}]", length);

    memcpy(&m_buffer[m_recv_len], buffer, length);
    m_recv_len += length;

    int found_len = 0;
    Parse(socket, m_buffer, m_recv_len, found_len);
    m_recv_len -= found_len;

    if (m_recv_len > 0)
        memcpy(&m_buffer[0], &m_buffer[found_len], m_recv_len);
}

